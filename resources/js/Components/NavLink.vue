<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/vue3';

const props = defineProps({
    href: {
        type: String,
        required: true,
    },
    active: {
        type: Boolean,
    },
});

const classes = computed(() =>
    props.active
        ? 'flex items-center p-2 rounded-md text-white bg-primary hover:bg-primary focus:outline-none transition duration-150 ease-in-out'
        : 'flex items-center p-2 rounded-md text-gray-500 hover:text-white hover:bg-primary focus:outline-none transition duration-150 ease-in-out'
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
